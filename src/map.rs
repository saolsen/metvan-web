pub fn room_type(left: bool, right: bool, up: bool, down: bool) -> u8 {
    let mut result: u8 = 0;
    if left {
        result |= 1 << 0;
    }
    if right {
        result |= 1 << 1;
    }
    if up {
        result |= 1 << 2
    }
    if down {
        result |= 1 << 3
    }
    result
}

